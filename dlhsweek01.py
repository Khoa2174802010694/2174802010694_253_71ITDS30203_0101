"""# 8. Tạo các biến xếp loại XL1, XL2 và XL3 dựa trên TBM1, TBM2 và TBM3
• Nhỏ hơn 5.0 xếp loại: yếu (kí hiệu là Y)
• Từ 5.0 đến dưới 6.5: trung bình (kí hiệu là TB)
• Từ 6.5 đến dưới 8.0: khá (kí hiệu là K)
• Từ 8.0 đến dưới 9.0: giỏi (kí hiệu là G)
• Từ 9.0 trở lên: xuất sắc (kí hiệu là XS)
"""

# Hàm xếp loại học lực

def xep_loai(tbm):

    if tbm < 5.0:
        return 'Y'

    elif tbm < 6.5:
        return 'TB'

    elif tbm < 8.0:
        return 'K'

    elif tbm < 9.0:
        return 'G'

    else:
        return 'XS'


# Tạo các cột xếp loại

df['XL1'] = df['TBM1'].apply(xep_loai)
df['XL2'] = df['TBM2'].apply(xep_loai)
df['XL3'] = df['TBM3'].apply(xep_loai)

# Hiển thị kết quả

print(df[['TBM1', 'XL1', 'TBM2', 'XL2', 'TBM3', 'XL3']].head(10))

"""# 9. Tạo các biến US_TBM1, US_TBM2 và US_TBM3 để chuyển điểm trung bình các năm lớp 10, 11 và 12 từ thang điểm 10 của Việt Nam sang thang điểm 4 của Mỹ. Sử dụng phương pháp Min-Max Normalization"""

# Chuyển đổi từ thang điểm 10 sang thang điểm 4

