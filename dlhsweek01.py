"""# 10. Tạo biến kết quả xét tuyển (kí hiệu là KQXT) nhằm xác định sinh viên đậu (giá trị “1”) và rớt ( giá trị “0”) vào các khối dựa trên điểm DH1, DH2 và DH3 như sau
• Với khối A, A1 nếu [(DH1*2 + DH2 + DH3)/4] lớn hơn hoặc bằng 5.0 thì đậu,
ngược lại là rớt

• Với khối B nếu [(DH1 + DH2*2 + DH3)/4] lớn hơn hoặc bằng 5.0 thì đậu, ngược
lại là rớt

• Với khối khác nếu [(DH1+ DH2 + DH3)/3] lớn hơn hoặc bằng 5.0 thì đậu, ngược
lại là rớt
"""

# Hàm xét tuyển

def xet_tuyen(row):

    khoi = row['KT']

    # Khối A, A1
    if khoi in ['A', 'A1']:

        diem = (row['DH1'] * 2 + row['DH2'] + row['DH3']) / 4

    # Khối B
    elif khoi == 'B':

        diem = (row['DH1'] + row['DH2'] * 2 + row['DH3']) / 4

    # Các khối khác
    else:

        diem = (row['DH1'] + row['DH2'] + row['DH3']) / 3


    # Xét đậu/rớt
    if diem >= 5.0:
        return 1
    else:
        return 0


# Tạo cột KQXT

df['KQXT'] = df.apply(xet_tuyen, axis=1)

# Hiển thị kết quả

print(df[['KT', 'DH1', 'DH2', 'DH3', 'KQXT']].head(10))

